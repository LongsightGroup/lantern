import { assertEquals } from '@std/assert';
import { APP_WRITER_EVALUATION_PROMPTS } from './evaluation_corpus.ts';
import { runAppWriterEvaluationPrompt } from './evaluation_runner.ts';
import { buildInitializedAppWriterWorkspace } from './workspace_initialization.ts';
import type { AppWriterWorkspaceRunner } from './workspace_runner.ts';
import type { AppPackageGenerationResult, AppWriterStarterId } from './types.ts';
import { buildValidSimpleActivityFiles } from '../test_helpers/app_writer_generated_package.ts';
import { createInMemoryPackageReviewRepository } from '../test_helpers/package_review.ts';
import { LANTERN_APP_CSS } from '../styles/lantern_app_css.ts';
import { PICO_CSS } from '../styles/pico_css.ts';

Deno.test('app writer evaluation runner tracks harness outcomes across the prompt corpus', async () => {
  const results = [];

  for (const prompt of APP_WRITER_EVALUATION_PROMPTS) {
    results.push(
      await runAppWriterEvaluationPrompt({
        prompt,
        repository: createInMemoryPackageReviewRepository(),
        workspaceRunner: createCorpusFakeWorkspaceRunner(),
        previewer: {
          preview(_input) {
            return Promise.resolve({
              validationFindings: [],
              assertionCount: 1,
              passedAssertionCount: 1,
              runtimeLog: [],
              summary: 'Passed 1/1 preview assertions.',
            });
          },
        },
        now: createClock([
          '2026-05-14T12:00:00.000Z',
          '2026-05-14T12:00:01.000Z',
          '2026-05-14T12:00:02.000Z',
          '2026-05-14T12:00:03.000Z',
          '2026-05-14T12:00:04.000Z',
          '2026-05-14T12:00:05.000Z',
          '2026-05-14T12:00:06.000Z',
          '2026-05-14T12:00:07.000Z',
        ]),
      }),
    );
  }

  assertEquals(
    results.map((result) => result.status),
    APP_WRITER_EVALUATION_PROMPTS.map(() => 'passed'),
  );
  assertEquals(
    results.map((result) => result.selectedStarterId),
    APP_WRITER_EVALUATION_PROMPTS.map((prompt) => prompt.expectedStarterId),
  );
  assertEquals(
    results.every((result) => result.previewPassed),
    true,
  );
});

function createCorpusFakeWorkspaceRunner(): AppWriterWorkspaceRunner {
  return {
    initialize(input) {
      return Promise.resolve(buildInitializedAppWriterWorkspace(input));
    },
    plan(input) {
      const result = buildGenerationResult(input.selectedStarterId);

      return Promise.resolve({
        normalizedRequest: result.normalizedRequest,
        appPlan: result.appPlan,
        selectedStarterId: result.selectedStarterId,
        progressUpdates: result.progressUpdates,
        notes: result.notes,
      });
    },
    author(input) {
      const result = buildGenerationResult(input.selectedStarterId);

      return Promise.resolve({
        files: result.files,
        progressUpdates: [],
        notes: [],
        validationFindings: result.validationFindings,
      });
    },
    repair(_input) {
      return Promise.reject(new Error('Evaluation fake workspace runner has no repair result.'));
    },
  };
}

function buildGenerationResult(starterId: AppWriterStarterId): AppPackageGenerationResult {
  const appId = starterId === 'browser-autograder' ? 'web-check' : 'phonics-match';

  return {
    normalizedRequest: {
      learningGoal: 'Practice the requested skill.',
      audience: 'Learners',
      contentSummary: 'Generated evaluation prompt content.',
      requestedActivity: 'practice',
      constraints: [],
      missingInformation: [],
      safeToGenerate: true,
    },
    appPlan: {
      appId,
      title: starterId === 'browser-autograder' ? 'Web Check' : 'Phonics Match',
      description: 'Generated evaluation app.',
      learningGoal: 'Practice the requested skill.',
      audience: 'Learners',
      activityType: starterId === 'browser-autograder' ? 'practice' : 'matching',
      learnerFlow: ['Open activity.', 'Complete task.'],
      contentModel: {},
      capabilities: starterId === 'browser-autograder'
        ? ['read_activity_content', 'submit_evidence_artifact', 'finalize_attempt']
        : ['read_activity_content', 'submit_attempt_event', 'finalize_attempt'],
      grading: {
        mode: starterId === 'browser-autograder' ? 'browser' : 'completion',
        maxScore: 100,
        scoringSummary: 'Evaluation scoring.',
      },
      attemptEvents: [
        {
          when: 'on completion',
          eventType: 'complete',
          questionIdPattern: 'activity',
        },
      ],
      previewTests: ['renders title'],
      accessibilityNotes: ['Use semantic text.'],
      riskNotes: [],
    },
    selectedStarterId: starterId,
    files: starterId === 'browser-autograder'
      ? buildValidBrowserAutograderFiles()
      : buildValidSimpleActivityFiles(),
    progressUpdates: [
      {
        stage: 'planning_app',
        message: 'Planning the evaluation app structure.',
      },
    ],
    notes: ['Generated by corpus fake workspace runner.'],
    validationFindings: [],
  };
}

