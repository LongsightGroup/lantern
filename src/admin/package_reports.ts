import type { PackageVersionRecord } from '../package_review/types.ts';
import type {
  InstructorReport,
  InstructorReportDailyActivity,
  InstructorReportEventMix,
  InstructorReportItemActivity,
  InstructorReportScoreBucket,
  InstructorReportStudent,
  InstructorReportStudentSignal,
} from '../reports/instructor_report.ts';
import {
  type AdminNotice,
  escapeHtml,
  formatDate,
  formatDateTime,
  renderAdminLayout,
} from './layout.ts';
import { renderPackagePageNav } from './package_navigation.ts';

export function renderPackageReportsPage(input: {
  appId: string;
  appTitle: string;
  history: PackageVersionRecord[];
  report: InstructorReport;
  notice?: AdminNotice | null;
}): string {
  return renderAdminLayout({
    title: `${input.appTitle} Reports`,
    eyebrow: 'Instructor reports',
    heading: input.appTitle,
    intro: 'Learner progress from Lantern-managed attempts and attempt events.',
    activePath: '/admin/packages',
    breadcrumbs: [
      { label: 'Apps', href: '/admin/packages' },
      { label: input.appTitle, href: `/admin/packages/${input.appId}` },
      { label: 'Reports' },
    ],
    notice: input.notice ?? null,
    pageNav: renderPackagePageNav({
      appId: input.appId,
      history: input.history,
      currentSection: 'reports',
    }),
    body: input.report.summary.attemptCount === 0
      ? renderEmptyReport()
      : `${renderReportSummary(input.report)}
          ${renderClassHealthSection(input.report)}
          ${renderDailyActivitySection(input.report.dailyActivity)}
          ${renderReportInsightsSection(input.report)}
          ${renderStudentSection(input.report.students)}
          ${renderRecentAttemptsSection(input.report)}`,
  });
}

function renderEmptyReport(): string {
  return `<section class="panel">
    <div class="panel-body empty-state">
      <div class="stack">
        <p class="section-label">No learner attempts yet</p>
        <h2>Reports appear after students launch the approved app.</h2>
        <p>Lantern excludes admin preview launches here. The report uses only learner attempts, GatewayApp attempt events, local progress writes, finalization, and grade publication records from the governed runtime.</p>
      </div>
    </div>
  </section>`;
}

function renderReportSummary(report: InstructorReport): string {
  const summary = report.summary;

  return `<section class="panel">
    <div class="panel-body stack">
      <div class="panel-header">
        <div class="stack">
          <p class="section-label">Class activity</p>
          <h2>Reportable progress</h2>
          <p>Counts come from learner launches and GatewayApp events. Preview launches stay out of this report.</p>
        </div>
        <p class="micro muted">Updated ${escapeHtml(formatDateTime(report.generatedAt))}</p>
      </div>
      <div class="facts">
        ${renderFact('Learners', String(summary.learnerCount))}
        ${renderFact('Attempts', String(summary.attemptCount))}
        ${renderFact('Completed', String(summary.completedAttemptCount))}
        ${renderFact('In progress', String(summary.inProgressAttemptCount))}
        ${renderFact('Answer events', String(summary.answerEventCount))}
        ${renderFact('Progress events', String(summary.progressEventCount))}
        ${renderFact('Completion events', String(summary.completeEventCount))}
        ${renderFact('Completion rate', formatPercent(summary.completionRatePercent))}
        ${renderFact('Average score', formatPercent(summary.averageScorePercent))}
        ${renderFact('Latest activity', formatDateTime(summary.latestActivityAt))}
      </div>
    </div>
  </section>`;
}

function renderClassHealthSection(report: InstructorReport): string {
  const followUpCount = report.students.filter(
    (student) => student.signal === 'needs_follow_up',
  ).length;
  const topItem = report.itemActivity[0] ?? null;

  return `<section class="panel">
    <div class="panel-body stack">
      <div class="panel-header">
        <div class="stack">
          <p class="section-label">Class health</p>
          <h2>Instructor takeaways</h2>
          <p>These signals summarize learner progress, completion, and the activity evidence generated by this app.</p>
        </div>
      </div>
      <div class="report-health-grid">
        <div class="report-health-primary">
          ${renderCompletionMeter(report.summary.completionRatePercent)}
          <div class="stack">
            <h3>${escapeHtml(formatPercent(report.summary.completionRatePercent))} complete</h3>
            <p class="micro muted">${
    escapeHtml(
      `${report.summary.completedAttemptCount} of ${report.summary.attemptCount} learner attempts are complete.`,
    )
  }</p>
          </div>
        </div>
        <ul class="report-takeaways">
          <li><strong>${escapeHtml(String(followUpCount))}</strong> ${
    escapeHtml(
      pluralize(followUpCount, 'learner'),
    )
  } may need follow-up.</li>
          <li><strong>${
    escapeHtml(
      String(report.summary.scoredAttemptCount),
    )
  }</strong> ${
    escapeHtml(
      pluralize(report.summary.scoredAttemptCount, 'attempt'),
    )
  } produced a reportable score.</li>
          <li><strong>${
    escapeHtml(
      String(report.summary.totalEventCount),
    )
  }</strong> governed runtime ${
    escapeHtml(
      pluralize(report.summary.totalEventCount, 'event'),
    )
  } recorded across answers, progress, and completions.</li>
          <li>${renderTopItemTakeaway(topItem)}</li>
        </ul>
      </div>
    </div>
  </section>`;
}

